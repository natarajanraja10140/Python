#String Manipulation And Functions

a ="Jesus World"
print(type(a))
print("Is Upper : ",a.upper())
print("Is lower : ",a.lower())
print("Is capitalized : ",a.capitalize())
print("Is title : ",a.title())
print("Count Of s : ",a.count("s"))
print("Length of a : ",len(a))
print("Is a Endswith ld : ",a.endswith("ld"))
print("Is a Endswith es : ",a.endswith("es"))
print("Find s in a : ",a.find("s"))
print("Find s after index 1 : ",a.find("s",1))
print("Replace s as e : ",a.replace("s","e"))
print("check if a is upper : ",a.isupper())
print("check if a is lower : ",a.islower())
print("check if a is title : ",a.istitle())
print("check if a consist Numeric : ",a.isalnum())
print("Check A is ALpha : ",a.isalpha())   #If A consist number it show false

